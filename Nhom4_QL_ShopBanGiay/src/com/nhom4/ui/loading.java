        new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int value = prbLoad.getValue();
                if (value < prbLoad.getMaximum()) {
                    prbLoad.setValue(value + 20);
                } else {
                    loading.this.setVisible(false);
                    
                }
            }
        }).start();
