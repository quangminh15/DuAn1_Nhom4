    public void TimKiem() {
        DefaultTableModel model = (DefaultTableModel) tblkhachHang.getModel();
        model.setRowCount(0);
        try {
            String khh = txttimKiem.getText();
            List<KhachHang> list = dao.selectByKeyword(khh);
            for (KhachHang kh : list) {
                Object[] data = {
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getSDT(),
                    kh.getDiaChi(),};
                model.addRow(data);
            }
        } catch (Exception e) {
            MsgBox.alert(this, e.getMessage());
        }
        this.clearForm();
        this.row = -1;
        updateStatus();

    }
