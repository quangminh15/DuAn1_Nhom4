    public void delete() {
        String kh = txtmaKhachHang.getText();
        if (MsgBox.confirm(this, "Bạn thực sự muốn xóa khách hàng này")) {
            try {
                dao.delete(kh);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xóa thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa thất bại");
            }
        }
    }

    public void clearForm() {
        KhachHang kh = new KhachHang();
        this.setForm(kh);
        this.row = -1;
        this.updateStatus();
    }

    public void setForm(KhachHang kh) {
        txtmaKhachHang.setText(String.valueOf(kh.getMaKH()));
        txttenKhachHang.setText(kh.getTenKH());
        txtEmail.setText(kh.getEmail());
        txtMatKhau.setText(kh.getMatKhau());
        txtsoDienThoai.setText(kh.getSDT());
        txtdiaChi.setText(kh.getDiaChi());
    }

    KhachHang getForm() {
        KhachHang kh = new KhachHang();
//        kh.setMaKH(Integer.valueOf(txtmaKhachHang.getText()));
        kh.setTenKH(txttenKhachHang.getText());
        kh.setEmail(txtEmail.getText());
        kh.setMatKhau(txtMatKhau.getText());
        kh.setSDT(txtsoDienThoai.getText());
        kh.setDiaChi(txtdiaChi.getText());
        kh.setXoa(true);
        return kh;
    }

    public void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblkhachHang.getRowCount() - 1);
//        Trạng thái form
        btnThem.setEnabled(edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
// Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
        txtOFF();
    }

    public void edit() {
        int makh = (int) tblkhachHang.getValueAt(this.row, 0);
        KhachHang kh = dao.selectById(makh);
        this.setForm(kh);
        this.updateStatus();
    }

    public void first() {
        this.row = 0;
        tblkhachHang.setRowSelectionInterval(row, row);
        this.edit();
    }

    public void prev() {
        if (this.row > 0) {
            this.row--;
            tblkhachHang.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    public void next() {
        if (this.row < tblkhachHang.getRowCount() - 1) {
            this.row++;
            tblkhachHang.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    public void last() {
        this.row = tblkhachHang.getRowCount() - 1;
        tblkhachHang.setRowSelectionInterval(row, row);
        this.edit();
    }

