    private void init() {
        this.row = -1;
        fillTable();
        this.updateStatus();
        btnLuu.setEnabled(false);
    }
    
    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNCC.getModel();
        model.setRowCount(0);
        try {
            List<NhaCungCap> list = dao.selectAll(); 
            for (NhaCungCap nv : list) {
                Object[] data = {
                    nv.getMaNCC(),
                    nv.getTenNCC()
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    public void insert() {
            NhaCungCap ncc = getForm();
            try {
                dao.insert(ncc);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công");
                updateStatus();
                them = 0;
                btnLuu.setEnabled(false);
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại");
                updateStatus();
                them = 0;
                btnLuu.setEnabled(false);
            }
        
    }
    
    public void update() {
        NhaCungCap nv = getForm();
        
            try {
                dao.update(nv);
                this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công");
                updateStatus();
                them = 0;
                btnLuu.setEnabled(false);
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại");
                updateStatus();
                them = 0;
                btnLuu.setEnabled(false);
            }
        
    }
    
    
    public void delete() {
            String ncc = txtMaNCC.getText();
            if (MsgBox.confirm(this, "Bạn thực sự muốn xóa nhà cung cấp này")) {
                try {
                    dao.delete(ncc);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại");
                }
            }
    }
    
    public void clearForm() {
        NhaCungCap ncc = new NhaCungCap();
        this.setForm(ncc);
        this.row = -1;
        this.updateStatus();
    }
    
    public void setForm(NhaCungCap ncc) {
        txtMaNCC.setText(ncc.getMaNCC());
        txtTenNCC.setText(ncc.getTenNCC());
    }
    
    NhaCungCap getForm() {
        NhaCungCap ncc = new NhaCungCap();
        ncc.setMaNCC(txtMaNCC.getText());
        ncc.setTenNCC(txtTenNCC.getText());
        return ncc;
    }
    
    public void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNCC.getRowCount() - 1);
//        Trạng thái form
        btnThem.setEnabled(edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
// Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPre.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
        txtOFF();
    }
    
    public void edit() {
        String mancc = (String) tblNCC.getValueAt(this.row, 0);
        NhaCungCap ncc = dao.selectById(mancc);
        this.setForm(ncc);
        this.updateStatus();
    }
    
    public void Dislay(int i) {
        NhaCungCap v = listNCC.get(i);
        txtMaNCC.setText(v.getMaNCC());
        txtTenNCC.setText(v.getTenNCC());
    }
    
    public void first() {
        try {
            this.row = 0;
        tblNCC.setSelectionBackground(new Color(0, 156, 222));
        tblNCC.setRowSelectionInterval(row, row);
        this.edit();
        Dislay(row);
        } catch (Exception e) {
            return;
        }
        
    }
    
    public void prev() {
        try {
            if (this.row > 0) {
            this.row--;
            this.edit();
        }
        tblNCC.setSelectionBackground(new Color(0, 156, 222));
        tblNCC.setRowSelectionInterval(row, row);
        Dislay(row);
        } catch (Exception e) {
            if (row < 0) {
            return;
        }
        }
    }
    
    public void next() {
        try {
            if (this.row < tblNCC.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
        tblNCC.setSelectionBackground(new Color(0, 156, 222));
        tblNCC.setRowSelectionInterval(row, row);
        Dislay(row);
        } catch (Exception e) {
            if (this.row == tblNCC.getRowCount() - 1) {
            return;
        }
        }
    }
    
    public void last() {
        try {
            this.row = tblNCC.getRowCount() - 1;
        this.edit();
        tblNCC.setSelectionBackground(new Color(0, 156, 222));
        tblNCC.setRowSelectionInterval(row, row);
        Dislay(row);
        } catch (Exception e) {
            return;
        }
        
    }
    
    public void txtOFF(){
        txtMaNCC.setEditable(false);
        txtTenNCC.setEditable(false);
    }
    
    public void txtON(){
        txtMaNCC.setEditable(true);
        txtTenNCC.setEditable(true);
    }
    
    public boolean check(){
        if(txtMaNCC.getText().equals("")){
            MsgBox.alert(this, "Không được để trống mã nhà cung cấp");
            txtMaNCC.requestFocus();
            return false;
        } else if (txtMaNCC.getText().length() > 0 && txtMaNCC.getText().length() < 3) {
            MsgBox.alert(this, "Mã nhà cung cấp phải nhập ít nhất 3 ký tự");
            txtMaNCC.requestFocus();
            return false;
        } else if(txtTenNCC.getText().equals("")){
            MsgBox.alert(this, "Không được để trống tên nhà cung cấp");
            txtTenNCC.requestFocus();
            return false;
        }
        return true;
    }
