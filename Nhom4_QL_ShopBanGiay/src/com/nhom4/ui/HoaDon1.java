    private void initTable() {
        DefaultTableModel model = (DefaultTableModel)  tblHoaDon.getModel();
        String[] cols = new String[]{"Ma HD", "Ma NV", "Ma KH", "Ngay Lap", "Thanh Tien", "Ma KM"};
        model.setColumnIdentifiers(cols);
    }
    public void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        try {
            List<HoaDon> list = hdDAO.selectAll(); // Đọc dữ liệu từ CSDL
            for (HoaDon hd : list) {
                Object[] data = {
                    hd.getMaHD(),
                    hd.getMaNV(),
                    hd.getMaKH(),
                    hd.getNgayBan(),
                    hd.getThanhTien(),
                    hd.getMaKM()
                };
                model.addRow(data);
            }
        } catch (Exception e) {
        }
    }
    
