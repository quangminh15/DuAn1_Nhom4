    public boolean check() {
         boolean check = true;
        for (int i = 0; i < listHD.size(); i++) {
            if (listHD.get(i).getMaHD().equalsIgnoreCase(txtMaHD.getText())) {
                check = false;
            }
        }

        if (txtMaHD.getText().equals("")) {
            MsgBox.alert(this, "Khong Bo Trong MA HOA DON");
            return false;
        }
        if (check == false) {
            MsgBox.alert(this, "MA HOA DON");
            return false;
        }
        return true;
    }

